class LongType {
    public static void main(String[] args) {
        long a = -1000000000;
        long b = 1000000000;
        System.out.println("The value of a : " + a);
        System.out.println("The value of b : " + b);
        System.out.println("The sum of a and b : " + (a + b));
    }
}
